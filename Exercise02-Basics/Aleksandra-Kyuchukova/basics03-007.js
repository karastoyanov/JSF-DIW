"use strict"
/*
BASICS03-007: Declare a constant and assign one digit. Print on one line the constant, the 
power of two (N2 ), the power of three (N3 ) on the console.
*/

const NUMONE = 5;

let powerofTwo = NUMONE ** 2;
let powerofThree = NUMONE ** 3;

console.log(`Constant: ${NUMONE}, The power of 2: ${powerofTwo}, The power of three: ${powerofThree}`);