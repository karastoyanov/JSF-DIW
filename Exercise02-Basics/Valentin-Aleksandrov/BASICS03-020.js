/* Declare a variable. Assign an integer number. Print on the console the 
variable, the binary, octal and hexadecimal representation. */

let n = 44;

console.log(`Decimal: ${n}`);
console.log(`Binary: ${n.toString(2)}`);
console.log(`Octal: ${n.toString(8)}`);
console.log(`Hexadecimal: ${n.toString(16).toUpperCase()}`);
