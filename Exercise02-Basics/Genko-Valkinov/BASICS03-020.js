// BASICS03-020: Declare a variable. Assign an integer number. Print on the console the 
// variable, the binary, octal and hexadecimal representation.

let firstNumber = 17;

console.log(`Variable: ${firstNumber}`);
console.log(`Binary: ${firstNumber.toString(2)}`);
console.log(`Octal: ${firstNumber.toString(8)}`);
console.log(`Hexadecimal: ${firstNumber.toString(16)}`);
