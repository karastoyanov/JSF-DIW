/**
 * DTYPES02-008: Define 5 bigint variables and print them in hexadecimal number system.
 */

let bigIntA = 255n;
let bigIntB = 555n;
let bigIntC = 1055n;
let bigIntD = 1024n;
let bigIntE = 2n;

console.log(`BigInt in Hexadecimal:`);
console.log(`bigIntA: ${bigIntA} ${bigIntA.toString(16)}`);
console.log(`bigIntB: ${bigIntB} ${bigIntB.toString(16)}`);
console.log(`bigIntC: ${bigIntC} ${bigIntC.toString(16)}`);
console.log(`bigIntD: ${bigIntD} ${bigIntD.toString(16)}`);
console.log(`bigIntE: ${bigIntE} ${bigIntE.toString(16)}`);
