/*
What is the result of the following arithmetic operations: 
1) NaN++ 
2) NaN - NaN 
3) Infinity - Infinity 
4) 5 - true 
5) Boolean(true-true)*true
*/

/*
1) NaN++                     > NaN
2) NaN - NaN                 > NaN
3) Infinity - Infinity       > NaN
4) 5 - true                  > 4 
5) Boolean(true-true)*true   > 0
*/