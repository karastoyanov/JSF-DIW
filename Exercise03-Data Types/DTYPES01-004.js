/* DTYPES01-004: Define two multi-line string variables in three ways. 
(hint: use single quotes, double quotes, string literals). */

// Method 1: Using Single Quotes 
let singleQuoteString1 = 'This is a multi-line string\n using single quotes. ';
let singleQuoteString2 = 'Here is another example of a\nmulti-line string using single quotes';

// Method 2: Using Double Quotes
let doubleQuoteString1 = "This is a multi-line string\n using double quotes.";
let doubleQuoteString2 = "Here is another example of a\nmulti-line string using double quotes.";

// Method 3: Using String Literals
let literalString1 = `This is a multi-line string
using string literals. You can simply press enter to
create a new line within the string.`;

let literalString2 = `Here is another example of a
multi-line string using string literals.`;

