/* Write a program that determines whether a given number is positive or 
negative. */

let number = 0;

if (number > 0) {
    console.log(`The number ${number} is positive.`);
} else if (number < 0) {
    console.log(`The number ${number} is negative.`);
} else {
    console.log(`The number ${number} is 0.`)

}
